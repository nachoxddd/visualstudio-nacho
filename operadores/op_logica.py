# Operador Y o AND es verdadero cuando ambos valores son verdaderos
print("Operador Y")
print(True & True)
print(True & False)
print(False & True)
print(False & False)

# Operador O u AND, ES V CUANDO AL MENOS UN VALOR ES VERDADERO
print("Operador O")
print(True | True)
print(True | False)
print(False | False)
print(False | True)

# Negacion NOT
print("Negacion, NOT")
print (not True)
print (not False)