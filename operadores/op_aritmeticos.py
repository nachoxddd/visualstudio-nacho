# Deficion variable
numero_1 = 5
numero_2 = 7
numero_3 = 15

nombre = "Martin "
apellido = "Marqueando"

# Operador SUMA
suma= numero_1 + numero_2
print(suma)

# concatenacion

nombre_completo = nombre + apellido 
print(nombre_completo)

# Operador RESTA
resta = numero_1 - numero_2
print(resta)
print(type(resta))

# Operador multiplicacion

multiplicacion = numero_1 * numero_2
print(multiplicacion)

# Exponente

exponente = numero_1 ** numero_2 # 5 ELEVADO A 7
print(exponente)
print(type(exponente))

#division

division = numero_3 / numero_1 
print(division)
print(type(division))

# division baja

division_baja = numero_3 // numero_2
print(division_baja)
print(type(division_baja))